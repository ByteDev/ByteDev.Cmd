namespace ByteDev.Cmd.Logging
{
    /// <summary>
    /// Represents the logging level.
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Debug logging level.
        /// </summary>
        Debug = 1,

        /// <summary>
        /// Information logging level.
        /// </summary>
        Info = 2,

        /// <summary>
        /// Warning logging level.
        /// </summary>
        Warning = 3,

        /// <summary>
        /// Error logging level.
        /// </summary>
        Error = 4,

        /// <summary>
        /// Critical error logging level.
        /// </summary>
        Critical = 5
    }
}